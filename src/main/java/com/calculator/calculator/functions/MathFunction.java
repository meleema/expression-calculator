package com.calculator.calculator.functions;

/**
 * Функциональный интерфейс для математических функций.
 * Определяет контракт для вычисления математических функций с переменным числом аргументов.
 * Используется в {@link FunctionRegistry} для унифицированного выполнения функций.
 * 
 * @author Yarovaya Maria
 * @version 1.0
 */
@FunctionalInterface
public interface MathFunction {
    /**
     * Вычисляет значение математической функции для заданных аргументов.
     * Реализации должны обеспечивать корректную обработку аргументов и возвращать результат вычисления.
     *
     * @param args аргументы функции в виде массива значений типа double.
     *             Количество и семантика аргументов зависят от конкретной функции.
     * @return результат вычисления функции как значение типа double
     * @throws IllegalArgumentException если аргументы не удовлетворяют требованиям функции
     *                                  (например, отрицательное число для квадратного корня,
     *                                  неположительное число для логарифма и т.д.)
     */
    double apply(double... args);
}