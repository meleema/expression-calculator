package com.calculator.calculator;

/**
 * Перечисление типов токенов для лексического анализа математических выражений.
 * Определяет категории лексем, которые могут встречаться в математическом выражении.
 * Используется классом {@link Token} для классификации элементов выражения.
 * 
 * @author Yarovaya Maria 
 * @version 1.0
 */
public enum TokenType {
    /**
     * Числовой литерал. Может быть целым числом или числом с плавающей точкой.
     * Примеры: "123", "45.67", "0.5"
     */
    NUMBER,
    
    /**
     * Идентификатор переменной. Имя переменной, значение которой должно быть предоставлено.
     * Примеры: "x", "y", "radius", "variable1"
     */
    VARIABLE,
    
    /**
     * Математический оператор. Символы, представляющие арифметические операции.
     * Поддерживаемые операторы: сложение (+), вычитание (-), умножение (*),
     * деление (/), возведение в степень (^)
     */
    OPERATOR,
    
    /**
     * Имя математической функции. Предопределенные или пользовательские функции.
     * Примеры: "sin", "cos", "sqrt", "log"
     */
    FUNCTION,
    
    /**
     * Левая круглая скобка. Используется для группировки выражений и вызовов функций.
     * Символ: '('
     */
    LEFT_PAREN,
    
    /**
     * Правая круглая скобка. Завершает группировку выражений и вызовов функций.
     * Символ: ')'
     */
    RIGHT_PAREN,
    
    /**
     * Разделитель аргументов. Используется для разделения аргументов в вызовах функций.
     * Символ: ','
     */
    COMMA,
    
    /**
     * Маркер конца выражения. Специальный токен, указывающий на завершение входной строки.
     * Генерируется парсером после обработки всех символов выражения.
     */
    EOF
}