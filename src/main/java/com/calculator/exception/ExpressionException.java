package com.calculator.exception;

/**
 * Специализированное исключение для ошибок в математических выражениях.
 * Содержит информацию о позиции ошибки в исходном выражении для точного указания места ошибки.
 * Используется парсером и вычислителем для передачи информации об ошибках синтаксиса и семантики.
 * 
 * @author Yarovaya Maria
 * @version 1.0
 */
public class ExpressionException extends RuntimeException {
    private final int position;
    
    /**
     * Конструктор исключения с сообщением и позицией ошибки.
     * Создает исключение с форматированным сообщением, включающим позицию ошибки.
     *
     * @param message описательное сообщение об ошибке
     * @param position позиция (индекс символа) в исходном выражении, где обнаружена ошибка
     */
    public ExpressionException(String message, int position) {
        super(message + " (позиция: " + position + ")");
        this.position = position;
    }
    
    /**
     * Возвращает позицию в выражении, где произошла ошибка.
     * Позиция представляет собой индекс символа в исходной строке выражения.
     * Используется для выделения места ошибки в пользовательском интерфейсе.
     *
     * @return позиция ошибки в выражении (индекс символа)
     */
    public int getPosition() {
        return position;
    }
}